Lemma char_Ohm : forall i G, 'Ohm_i(G) \char G.
Proof.
move=> i G; have sOmG: 'Ohm_i(G) \subset G.
  by rewrite gen_subG; apply/subsetP=> x; rewrite inE; case/andP.
apply/charP; split=> // f injf fG; apply/morphim_fixP => //.
