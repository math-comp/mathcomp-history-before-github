Lemma char_Mho : forall i G, 'Mho^i(G) \char G.
Proof.
move=> i G; have sMhoG: 'Mho^i(G) \subset G.
  rewrite gen_subG; apply/subsetP=> y; case/imsetP=> x Gx ->; exact: groupX.
